# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution(object):
    def reverseList(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        #iterative way:
        '''prev = None
        tail = head
        while tail is not None:
            temp = tail.next
            tail.next = prev
            prev = tail
            tail = temp
        return prev'''
        #recursive way:
        if head is None or head.next is None:
            return head
        p = self.reverseList(head.next)
        head.next.next = head
        head.next = None
        return p
